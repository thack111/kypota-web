package com.hacksnet.kypota.model;

public class ResultsSummary {
	
	private int logId;
	private String submittedEmail;
	private String submittedName;
	private int numQsoPoints;
	private int numParks;
	private int numPark2Park;
	private int numBonus;
	private int totalScore;
	
	public int getLogId() {
		return logId;
	}
	public void setLogId(int logId) {
		this.logId = logId;
	}
	public String getSubmittedEmail() {
		return submittedEmail;
	}
	public void setSubmittedEmail(String submittedEmail) {
		this.submittedEmail = submittedEmail;
	}
	public String getSubmittedName() {
		return submittedName;
	}
	public void setSubmittedName(String submittedName) {
		this.submittedName = submittedName;
	}
	public int getNumQsoPoints() {
		return numQsoPoints;
	}
	public void setNumQsoPoints(int numQsoPoints) {
		this.numQsoPoints = numQsoPoints;
	}
	public int getNumParks() {
		return numParks;
	}
	public void setNumParks(int numParks) {
		this.numParks = numParks;
	}
	public int getNumPark2Park() {
		return numPark2Park;
	}
	public void setNumPark2Park(int numPark2Park) {
		this.numPark2Park = numPark2Park;
	}
	public int getNumBonus() {
		return numBonus;
	}
	public void setNumBonus(int numBonus) {
		this.numBonus = numBonus;
	}
	public int getTotalScore() {
		return totalScore;
	}
	public void setTotalScore(int totalScore) {
		this.totalScore = totalScore;
	}
	
	
	
}
